CREATE PROCEDURE GRAPH_PRICE_RANGE()
BEGIN
	SELECT 
		  SUM(CASE WHEN PRICE >=0 AND PRICE <30000 THEN 1 ELSE 0 END) as '0 - 30k',
		  SUM(CASE WHEN PRICE >=30000 AND PRICE <60000 THEN 1 ELSE 0 END) as '30k -60k',
		  SUM(CASE WHEN PRICE >=60000 AND PRICE <90000 THEN 1 ELSE 0 END) as '60k -90k',
		  SUM(CASE WHEN PRICE >=90000 AND PRICE <110000 THEN 1 ELSE 0 END) as '90k-110k',
		  SUM(CASE WHEN PRICE >=110000 AND PRICE <130000 THEN 1 ELSE 0 END) as '110k-130k',
		  SUM(CASE WHEN PRICE >=130000 AND PRICE <150000 THEN 1 ELSE 0 END) as '130k-150k',
		  SUM(CASE WHEN PRICE >=150000 AND PRICE <180000 THEN 1 ELSE 0 END) as '150k-180k',
		  SUM(CASE WHEN PRICE >=180000 THEN 1 ELSE 0 END) as '180+'
	FROM ads;
END //
DELIMITER ;